package lesson3;

public class whileLoop {
    public static void main(String[] args) {
        int counter = 1;
        while (counter <= 20) {
            System.out.print (counter + " ");
            counter ++;
        }
        System.out.println();
        
        counter = 10;
        while (counter > 0) {
            System.out.print (counter + " ");
            counter --;
    }
}
}